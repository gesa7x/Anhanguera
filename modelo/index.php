            <h2>
                <a name="_Toc397953827">
                    Níveis de Acesso Usuário.
                </a>
            </h2>


            <section class="lista">
                <p>
                    <b>     Usuário – </b>
                    Nível mais baixo de usuário, podendo apenas visualizar as listas e fazer buscas, sem nenhuma permissão de alteração.
                </p>

                <p>
                    <b>   Administrador - </b>
                    Este usuário tem direitos de permissões nível médio, podendo fazer alterações em todos os módulos
                    com exceção do cadastro de Usuário e senha.
                </p>

                <p>
                    <b>   Administrador Master - </b>
                    Nível mais alto de permissões, podendo fazer alterações em todos os módulos incluindo usuários.
                </p>

            </section>

            <p>Para editar as permissões escolha o usuário clique em editar e vá até o ultimo campo.
