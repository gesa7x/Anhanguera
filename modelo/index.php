            <h1>
                <a name="_Toc397953823">
                    Cadastro de Serviço.
                </a>
            </h1>

            <p>
                <img border=0 width=581 height=515 id="Imagem 23" src="img/image009.png">
            </p>

            <section class="lista">
                <p>
                    <b>   Divisão – </b>
                    Escolha neste campo a divisão cadastrada anteriormente que será ligada ao serviço.
                </p>

                <p>
                    <b>      Serviço – </b>
                    Neste campo é descrito um nome para o nome de serviço.
                    <br>  (Exemplo: Material Microbiológico, biofármacos em célula animal)
                </p>

                <p>
                    <b>     Localização –  </b>
                    Localização do Prédio / Divisão.
                    <br>   (Exemplo: Prédio da Fundação Butantan, Centro de Biotecnologia, Prédio 38)
                </p>

                <p>
                    <b>   Descrição – </b>
                    Breve resumo da função da divisão, setor ou área.
                </p>

                <p>
                    <b>      NBS – </b>
                    Nomenclatura Brasileira de Serviços. De acordo com o registro nos órgãos públicos,
                    este numero define o tipo de material que sua divisão poderá manipular.
                </p>

                <p>
                    <span style="font-size:12px;">
                        Para mais informação sobre NBS acesse
